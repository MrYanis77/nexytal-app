export function buildCertificationOfficielle(entry) {
  const base = `${entry.repertoire === 'RS' ? 'rs' : 'rncp'}/${entry.code}`;
  return {
    ...entry,
    url: `https://www.francecompetences.fr/recherche/${base}/`,
  };
}

/** Remplace les mentions vagues par le code RNCP dans les chaînes de la fiche. */
export function harmonizeFormationText(json, id, cert) {
  const codeLabel = `${cert.repertoire} ${cert.code}`;
  const rncpDetail = `${cert.repertoire} ${cert.code} – ${cert.intituleOfficiel}`;

  const replaceInString = (str) => {
    if (typeof str !== 'string') return str;
    let s = str;
    s = s.replace(/RNCP\s*à\s*préciser/gi, `${cert.repertoire} ${cert.code}`);
    s = s.replace(/Niveau RNCP à préciser/gi, `${cert.repertoire} ${cert.code}`);
    s = s.replace(/Titre RNCP \(Niveau 5 à 6\)/g, rncpDetail);
    s = s.replace(/Titre professionnel RNCP – niveau 6 \(équivalent Bac\+3\/4\)/g, rncpDetail);
    s = s.replace(/Titre RNCP \(Niveau 5 à 6\)/g, rncpDetail);
    s = s.replace(/Certification reconnue par l’État\s*Niveau RNCP\b/g, `Certification ${codeLabel}`);
    s = s.replace(/RS\s*5596/g, `RS ${cert.code}`);
    return s;
  };

  const walk = (obj) => {
    if (Array.isArray(obj)) return obj.map(walk);
    if (obj && typeof obj === 'object') {
      return Object.fromEntries(Object.entries(obj).map(([k, v]) => [k, walk(v)]));
    }
    return replaceInString(obj);
  };

  const updated = walk(json[id]);

  if (updated.stats) {
    const certStat = updated.stats.find(
      (s) => s.label === 'Certification' || s.label === 'Compétences clés',
    );
    if (certStat && /RNCP|Titre RNCP|à préciser/i.test(certStat.value)) {
      certStat.value =
        cert.repertoire === 'RS'
          ? `${codeLabel} – éligible CPF`
          : `Titre professionnel ${codeLabel} (niveau ${cert.niveau})`;
    }
  }

  if (id === 'formations-concepteur-designer-ui' && updated.stats) {
    const certStat = updated.stats.find((s) => s.label === 'Certification');
    if (certStat) {
      certStat.value = `Titre professionnel RNCP ${cert.code} – niveau ${cert.niveau}`;
    }
  }

  return updated;
}
